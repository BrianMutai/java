public class increament {
    public static void main (String []args){
        int num1 =34;
        int num2 =67;
        //num1 = num1+10;
        num1 *=10;
        System.out.println(num1);
        //++num1;
        System.out.println(++num1);// pre increamenting
        //post imcreament
        System.out.println(num2--);
        System.out.println(num2);//calling the function to 66



    }
}
